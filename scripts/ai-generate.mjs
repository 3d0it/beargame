import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import {
  AI_TURNS,
  canonicalBoardSignature,
  describeBearStartCandidates,
  describeHunterSetupCandidates,
  POSITION_BY_INDEX,
  stateIndexFor,
  stateIndexForState,
  terminalInfoForState
} from '../web/game-ai-model.js';
import {
  HUNTER_LUNETTES,
  MAX_BEAR_MOVES
} from '../web/game-state-helpers.js';
import {
  evaluateBearTurnFromSuccessors,
  evaluateHuntersTurnFromSuccessors,
  listStateSuccessors
} from '../web/game-ai-tablebase-core.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, '../web/game-ai-table.js');

export async function generateAiTableModuleSource() {
  const outcome = new Uint8Array(POSITION_BY_INDEX.length * MAX_BEAR_MOVES * 2);
  const distance = new Uint8Array(POSITION_BY_INDEX.length * MAX_BEAR_MOVES * 2);

  for (let bearMoves = MAX_BEAR_MOVES - 1; bearMoves >= 0; bearMoves -= 1) {
    solveBearTurnLayer(outcome, distance, bearMoves);
    solveHunterTurnLayer(outcome, distance, bearMoves);
  }

  const lookupStateInfo = (state) => {
    const terminal = terminalInfoForState(state);
    if (terminal) return terminal;

    const stateIndex = stateIndexForState(state);
    if (stateIndex < 0) {
      throw new Error(`Invalid state lookup during AI table generation: ${canonicalBoardSignature(state)}`);
    }

    return {
      outcome: outcome[stateIndex],
      distance: distance[stateIndex]
    };
  };

  const bearStartRankings = Object.fromEntries(
    HUNTER_LUNETTES.map((lunette) => [
      lunette.join(','),
      describeBearStartCandidates(lunette, lookupStateInfo).map((candidate) => candidate.move.to)
    ])
  );
  const hunterLunetteRanking = describeHunterSetupCandidates((lunette) =>
    describeBearStartCandidates(lunette, lookupStateInfo)
  ).map((candidate) => candidate.lunette);

  const signature = computeSignature(outcome, distance, bearStartRankings, hunterLunetteRanking);

  return `// Generated by scripts/ai-generate.mjs. Do not edit by hand.
export const AI_TABLE_SIGNATURE = '${signature}';
export const AI_OUTCOME_BASE64 =
${chunkBase64(Buffer.from(outcome).toString('base64'))};
export const AI_DISTANCE_BASE64 =
${chunkBase64(Buffer.from(distance).toString('base64'))};
export const AI_SETUP_HUNTER_LUNETTE_RANKING = ${JSON.stringify(hunterLunetteRanking, null, 2)};
export const AI_SETUP_BEAR_START_RANKINGS = ${JSON.stringify(bearStartRankings, null, 2)};
`;
}

export async function writeAiTableModule() {
  const source = await generateAiTableModuleSource();
  await writeFile(OUTPUT_PATH, source, 'utf8');
  return source;
}

export async function checkAiTableModule() {
  const expected = await generateAiTableModuleSource();
  let current = '';
  try {
    current = await readFile(OUTPUT_PATH, 'utf8');
  } catch {
    return false;
  }
  return current === expected;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  if (process.argv.includes('--check')) {
    const matches = await checkAiTableModule();
    if (!matches) {
      throw new Error('Generated AI table is out of date. Run `npm run ai:generate`.');
    }
    console.log('AI table is up to date.');
  } else {
    await writeAiTableModule();
    console.log(`Generated ${OUTPUT_PATH}`);
  }
}

function solveBearTurnLayer(outcome, distance, bearMoves) {
  for (let positionIndex = 0; positionIndex < POSITION_BY_INDEX.length; positionIndex += 1) {
    const position = POSITION_BY_INDEX[positionIndex];
    const stateIndex = stateIndexFor(positionIndex, bearMoves, AI_TURNS.bear);
    const state = {
      hunters: position.hunters,
      bear: position.bear,
      bearMoves,
      turn: AI_TURNS.bear,
      phase: 'playing'
    };

    const terminal = terminalInfoForState(state);
    if (terminal) {
      outcome[stateIndex] = terminal.outcome;
      distance[stateIndex] = terminal.distance;
      continue;
    }

    const successorInfos = listStateSuccessors(state).map(({ nextState }) => {
      const nextTerminal = terminalInfoForState(nextState);
      if (nextTerminal) return nextTerminal;

      const nextStateIndex = stateIndexForState(nextState);
      return {
        outcome: outcome[nextStateIndex],
        distance: distance[nextStateIndex]
      };
    });
    const exact = evaluateBearTurnFromSuccessors(successorInfos);

    outcome[stateIndex] = exact.outcome;
    distance[stateIndex] = exact.distance;
  }
}

function solveHunterTurnLayer(outcome, distance, bearMoves) {
  for (let positionIndex = 0; positionIndex < POSITION_BY_INDEX.length; positionIndex += 1) {
    const position = POSITION_BY_INDEX[positionIndex];
    const stateIndex = stateIndexFor(positionIndex, bearMoves, AI_TURNS.hunters);
    const state = {
      hunters: position.hunters,
      bear: position.bear,
      bearMoves,
      turn: AI_TURNS.hunters,
      phase: 'playing'
    };

    const terminal = terminalInfoForState(state);
    if (terminal) {
      outcome[stateIndex] = terminal.outcome;
      distance[stateIndex] = terminal.distance;
      continue;
    }

    const successorInfos = listStateSuccessors(state).map(({ nextState }) => {
      const nextTerminal = terminalInfoForState(nextState);
      if (nextTerminal) return nextTerminal;

      const nextStateIndex = stateIndexForState(nextState);
      return {
        outcome: outcome[nextStateIndex],
        distance: distance[nextStateIndex]
      };
    });
    const exact = evaluateHuntersTurnFromSuccessors(successorInfos);

    outcome[stateIndex] = exact.outcome;
    distance[stateIndex] = exact.distance;
  }
}

function computeSignature(outcome, distance, bearStartRankings, hunterLunetteRanking) {
  let hash = 0x811c9dc5;
  hash = fnvBytes(hash, outcome);
  hash = fnvBytes(hash, distance);
  hash = fnvString(hash, JSON.stringify(bearStartRankings));
  hash = fnvString(hash, JSON.stringify(hunterLunetteRanking));
  return hash.toString(16).padStart(8, '0');
}

function fnvBytes(hash, bytes) {
  let next = hash >>> 0;
  for (let index = 0; index < bytes.length; index += 1) {
    next ^= bytes[index];
    next = Math.imul(next, 0x01000193) >>> 0;
  }
  return next >>> 0;
}

function fnvString(hash, value) {
  let next = hash >>> 0;
  for (let index = 0; index < value.length; index += 1) {
    next ^= value.charCodeAt(index);
    next = Math.imul(next, 0x01000193) >>> 0;
  }
  return next >>> 0;
}

function chunkBase64(value) {
  const lines = [];
  for (let index = 0; index < value.length; index += 120) {
    lines.push(`  '${value.slice(index, index + 120)}'`);
  }
  return `[\n${lines.join(',\n')}\n].join('');`;
}
